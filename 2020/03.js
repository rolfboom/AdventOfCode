const calculateRightPosition = (slopeIndex, right = 3, down = 1) => (slopeIndex / down) * right;
const getTerrainFromPosition = (position, slope) => slope.charAt(position % slope.length);

const getTreesTraverseSlope = (right = 3, down = 1) => {
    return slopes.reduce((accumulator, slope, index) => {
        if (index % down) {
            return accumulator;
        }
        return accumulator + (getTerrainFromPosition(calculateRightPosition(index, right, down), slope) === '#' ? 1 : 0);
    }, 0);
};

console.log('Answer one:', getTreesTraverseSlope(3, 1));

const treesPerSlope = [
    getTreesTraverseSlope(1, 1),
    getTreesTraverseSlope(3, 1),
    getTreesTraverseSlope(5, 1),
    getTreesTraverseSlope(7, 1),
    getTreesTraverseSlope(1, 2),
];

console.log('Answer two:', treesPerSlope.reduce((acc, trees) => acc * trees), treesPerSlope);